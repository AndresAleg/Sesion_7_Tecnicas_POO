    private void lstCitasValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstCitasValueChanged
        
        if (!evt.getValueIsAdjusting()) {
            String citaSeleccionada = lstCitas.getSelectedValue();
            Cita cita = buscarCita(citaSeleccionada);
            
            if (cita != null) {
                
                
                JFrameVisualizarCita visualizarCita = new JFrameVisualizarCita(cita);
                visualizarCita.setLocationRelativeTo(this);
                visualizarCita.setVisible(true);
                this.dispose();

            }
        }
    }//GEN-LAST:event_lstCitasValueChanged

