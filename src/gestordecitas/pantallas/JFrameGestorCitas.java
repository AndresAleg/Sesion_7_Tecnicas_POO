    public final void inicializarFormulario() {
        
        listaCitas = GestorDeCitas.listaCitas;
        
        modelListCitasShow =  new DefaultListModel<>();
        this.lstCitas.setModel(modelListCitasShow);
        modelListCitasShow.removeAllElements();
        
        for (Cita cita : listaCitas){
            modelListCitasShow.addElement(cita.mostrarDatos());
        }
    }
    
    private Cita buscarCita(String citaSeleccionada) {
        String[] campos = citaSeleccionada.split(" - ");
        
        for (Cita cita : listaCitas) {
            if (cita.getPaciente().getNombre().equals(campos[0]) &&
                    cita.getPaciente().getDni().equals(campos[1])) {
                return cita;
            }
        }
        return null;
    }

